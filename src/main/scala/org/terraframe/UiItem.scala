  val toolWeaponArmorMaxStacks: Short = 1
}

object Durability {
  val armorDurability: Short = 200
}

object UiItem {

  val defaultDurability: Short = 0
  val defaultSpeed: Double     = 0.175
  val M: Int                   = Int.MaxValue

  def toolDurability(item: UiItem): Short = item match {
    case a: ArmorUiItem  => a.durability
    case w: WeaponUiItem => w.durability
    case t: ToolUiItem   => t.durability
    case _               => 0
  }

  def blockDurabilityForTool(item: UiItem, Block: Block): Int = item match {
    case a: AxeUiItem     => a.blockDurability(Block)
    case p: PickaxeUiItem => p.blockDurability(Block)
    case _                => M
  }

  def armor(item: UiItem): Int = item match {
    case a: ArmorUiItem => a.armor
    case _              => 0
  }

  def image(item: UiItem): Option[BufferedImage] = item match {
    case imageUiItem: ImageUiItem => Some(imageUiItem.image)
    case _                        => None
  }

  def onImageItem(item: UiItem)(f: (ImageUiItem) => Unit): Unit = item match {
    case imageUiItem: ImageUiItem => f(imageUiItem)
    case _                        =>
  }

  def speed(item: UiItem): Double = item match {
    case t: ToolUiItem   => t.speed
    case w: WeaponUiItem => w.speed
    case _               => defaultSpeed
  }

  def isTool(item: UiItem): Boolean = item match {
    case _: ToolUiItem => true
    case _             => false
  }

  def blockForTool(item: UiItem): Block = item match {
    case b: BlockUiItem => b.Block
    case _              => AirBlock
  }

  def damage(item: UiItem): Int = item match {
    case w: WeaponUiItem => w.damage
    case t: ToolUiItem   => t.damage
    case _               => 0
  }

  def renderOverlayText(item: UiItem, dur: Short, mouseX: Int, mouseY: Int, g: Graphics2D, f: Font): Unit = {
    val toolDur = UiItem.toolDurability(item)
    g.setFont(f)
    g.setColor(Color.WHITE)

    if (toolDur === 0) {
      g.drawString(item.name, mouseX, mouseY)
    } else {
      g.drawString(item.name + " (" + (dur.toDouble / toolDur * 100).toInt + "%)", mouseX, mouseY)
    }
  }
}

