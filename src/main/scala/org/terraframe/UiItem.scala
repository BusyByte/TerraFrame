abstract class UiItem(val imageName: String, val name: String, val id: Short) {
  @inline def maxStacks: Short = UiMaxStacks.itemMaxStacks
}

sealed abstract class ImageUiItem(imageName: String, name: String, id: Short) extends UiItem(imageName, name, id) {
  lazy val image: BufferedImage = loadImage("items/" + imageName + ".png").get
}

sealed abstract class BlockUiItem(imageName: String, name: String, id: Short, val Block: Block)
    extends ImageUiItem(imageName, name, id)

sealed abstract class ToolUiItem(imageName: String,
                                 name: String,
                                 id: Short,
                                 val speed: Double,
                                 val damage: Int,
                                 val durability: Short)
    extends ImageUiItem(imageName, name, id) {
  @inline override def maxStacks = UiMaxStacks.toolWeaponArmorMaxStacks
}

sealed abstract class PickaxeUiItem(imageName: String,
                                    name: String,
                                    id: Short,
                                    speed: Double,
                                    damage: Int,
                                    durability: Short,
                                    val blockDurability: Map[Block, Int])
    extends ToolUiItem(imageName, name, id, speed, damage, durability)

sealed abstract class AxeUiItem(imageName: String,
                                name: String,
                                id: Short,
                                speed: Double,
                                damage: Int,
                                durability: Short,
                                val blockDurability: Map[Block, Int])
    extends ToolUiItem(imageName, name, id, speed, damage, durability)

sealed abstract class WeaponUiItem(imageName: String,
                                   name: String,
                                   id: Short,
                                   val speed: Double,
                                   val damage: Int,
                                   val durability: Short)
    extends ImageUiItem(imageName, name, id) {
  @inline override def maxStacks = UiMaxStacks.toolWeaponArmorMaxStacks
}
sealed abstract class ArmorUiItem(imageName: String, name: String, id: Short, val armor: Int)
    extends ImageUiItem(imageName, name, id) {
  @inline override def maxStacks = UiMaxStacks.toolWeaponArmorMaxStacks
  @inline def durability: Short  = Durability.armorDurability
}

